#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const root = resolve(import.meta.dirname, "..");
const contractPath = resolve(root, "packages/core/src/api/api-contract.ts");
const outPath = resolve(root, "docs/API_ROUTES.md");
const source = readFileSync(contractPath, "utf8");

const args = new Set(process.argv.slice(2));
const checkOnly = args.has("--check");

function unquote(value) {
  return value.replace(/\\"/g, '"');
}

function resolvePathExpression(expr) {
  const trimmed = expr.trim();
  if (/^"[^"]*"$/.test(trimmed)) return trimmed.slice(1, -1);
  if (trimmed === "DEFAULTS.api.cloudGraphql") return "/api/cloud/graphql";
  if (trimmed === "DEFAULTS.api.cloudRestPrefix") return "/api/cloud/";

  const cloudRest = trimmed.match(/^cloudRestPath\("([^"]+)"\)$/);
  if (cloudRest) {
    const normalized = cloudRest[1].replace(/^\/+/, "");
    const withoutApiPrefix = normalized.startsWith("api/") ? normalized.slice(4) : normalized;
    return `/api/cloud/${withoutApiPrefix}`;
  }

  throw new Error(`Unsupported path expression in API contract: ${expr}`);
}

function collectConstBlock(name) {
  const start = source.indexOf(`export const ${name} = {`);
  if (start < 0) throw new Error(`Could not find export const ${name}`);
  const tail = source.slice(start);
  const end = tail.indexOf("} as const");
  if (end < 0) throw new Error(`Could not find end of export const ${name}`);
  return tail.slice(0, end);
}

function collectRouteRows(block, kind) {
  const rows = [];
  const lineRe = /^\s*"([^"]+)":\s*\{\s*method:\s*"([^"]+)"\s*,\s*path:\s*([^,]+),.*?desc:\s*"((?:\\"|[^"])*)"/gm;
  let match;
  while ((match = lineRe.exec(block))) {
    rows.push({
      key: match[1],
      method: match[2],
      path: resolvePathExpression(match[3]),
      desc: unquote(match[4]),
      kind,
    });
  }
  return rows;
}

function groupName(key) {
  const prefix = key.split(".")[0];
  const names = {
    agentSettings: "Agent Settings",
    app: "App",
    auth: "Auth",
    channels: "Channels",
    chatSessions: "Chat Sessions",
    cloud: "Cloud Proxy",
    csBridge: "CS Bridge",
    deps: "Dependencies",
    doctor: "Doctor",
    events: "Events",
    extras: "Extras",
    fileDialog: "File Dialog",
    localModels: "Local Models",
    logs: "Logs",
    mobile: "Mobile Chat",
    models: "Models",
    oauth: "OAuth",
    pairing: "Pairing",
    providerKeys: "Provider Keys",
    sessionModel: "Session Model",
    settings: "Settings",
    skills: "Skills",
    stt: "STT",
    telemetry: "Telemetry",
    tools: "Tool Registry",
    usage: "Usage",
    uploads: "Uploads",
  };
  return names[prefix] ?? prefix;
}

const apiRows = collectRouteRows(collectConstBlock("API"), "REST");
const sseRows = collectRouteRows(collectConstBlock("SSE"), "SSE");
const cloudRestRows = collectRouteRows(collectConstBlock("CLOUD_REST"), "Cloud REST");
const rows = [...apiRows, ...cloudRestRows, ...sseRows];

const groups = new Map();
for (const row of rows) {
  const group = groupName(row.key);
  if (!groups.has(group)) groups.set(group, []);
  groups.get(group).push(row);
}

const header = `# API Routes

> Auto-generated from \`packages/core/src/api/api-contract.ts\`.
> Do not edit this file by hand. Run \`pnpm generate:api-routes\`.

`;

let markdown = header;
for (const [group, groupRows] of [...groups.entries()].sort(([a], [b]) => a.localeCompare(b))) {
  markdown += `## ${group}\n\n`;
  markdown += "| Key | Kind | Method | Path | Description |\n";
  markdown += "| --- | --- | --- | --- | --- |\n";
  for (const row of groupRows) {
    markdown += `| \`${row.key}\` | ${row.kind} | ${row.method} | \`${row.path}\` | ${row.desc} |\n`;
  }
  markdown += "\n";
}

markdown += `## Prefix Routes

| Key | Kind | Method | Prefix | Description |
| --- | --- | --- | --- | --- |
| \`cloud.rest\` | Prefix | * | \`/api/cloud/\` | Cloud REST proxy; strips \`/cloud\` and forwards with JWT. |
| \`media.static\` | Prefix | GET | \`/api/media/\` | Static media serving prefix. |
`;

if (checkOnly) {
  const existing = readFileSync(outPath, "utf8");
  if (existing !== markdown) {
    console.error("docs/API_ROUTES.md is out of date. Run pnpm generate:api-routes.");
    process.exit(1);
  }
  console.log("docs/API_ROUTES.md is up to date.");
} else {
  writeFileSync(outPath, markdown);
  console.log(`Wrote ${outPath}`);
}
