#!/usr/bin/env node

import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const vendorRoot = path.join(repoRoot, "vendor", "openclaw");
const distDir = path.join(vendorRoot, "dist");
const version = (await readFile(path.join(repoRoot, ".openclaw-version"), "utf8")).trim();

const schemaChunk = (await readdir(distDir)).find(
  (name) => name.startsWith("zod-schema-") && name.endsWith(".js"),
);
if (!schemaChunk) throw new Error("OpenClaw Zod schema bundle was not found; build vendor first");

const schemaModule = await import(pathToFileURL(path.join(distDir, schemaChunk)).href);
const openClawSchema = Object.values(schemaModule).find(
  (value) =>
    value &&
    typeof value === "object" &&
    typeof value.safeParse === "function" &&
    typeof value.toJSONSchema === "function",
);
if (!openClawSchema) throw new Error(`OpenClawSchema export was not found in ${schemaChunk}`);

const jsonSchema = openClawSchema.toJSONSchema({ unrepresentable: "any" });

function variants(schema) {
  return [schema, ...(schema.anyOf ?? []), ...(schema.oneOf ?? [])];
}

function schemaType(schema) {
  const values = new Set();
  for (const variant of variants(schema)) {
    if (Object.hasOwn(variant, "const")) values.add(JSON.stringify(variant.const));
    else if (Array.isArray(variant.enum)) {
      for (const value of variant.enum) values.add(JSON.stringify(value));
    } else if (variant.type === "array") values.add(`${schemaType(variant.items ?? {})}[]`);
    else if (variant.type) values.add(variant.type);
  }
  return [...values].join(" | ") || "any";
}

function constraints(schema) {
  const notes = [];
  for (const variant of variants(schema)) {
    if (variant.minimum !== undefined) notes.push(`min ${variant.minimum}`);
    if (variant.exclusiveMinimum !== undefined) notes.push(`> ${variant.exclusiveMinimum}`);
    if (variant.maximum !== undefined) notes.push(`max ${variant.maximum}`);
    if (variant.maxLength !== undefined) notes.push(`max length ${variant.maxLength}`);
    if (variant.pattern) notes.push(`pattern \`${variant.pattern.replaceAll("|", "\\|")}\``);
  }
  return [...new Set(notes)].join("; ");
}

function collectRows(schema, prefix, rows, seen = new Set()) {
  for (const variant of variants(schema)) {
    if (!variant || typeof variant !== "object" || seen.has(variant)) continue;
    seen.add(variant);
    const required = new Set(variant.required ?? []);
    for (const [key, child] of Object.entries(variant.properties ?? {})) {
      const configPath = prefix ? `${prefix}.${key}` : key;
      rows.push({
        path: configPath,
        type: schemaType(child),
        required: required.has(key),
        notes: constraints(child),
      });
      collectRows(child, configPath, rows, new Set(seen));
    }
    if (variant.additionalProperties && typeof variant.additionalProperties === "object") {
      const configPath = `${prefix}.*`;
      rows.push({
        path: configPath,
        type: schemaType(variant.additionalProperties),
        required: false,
        notes: "map value",
      });
      collectRows(variant.additionalProperties, configPath, rows, new Set(seen));
    }
  }
}

const rows = [];
collectRows(jsonSchema, "", rows);
const uniqueRows = [...new Map(rows.map((row) => [row.path, row])).values()];
const sections = new Map();
for (const row of uniqueRows) {
  const section = row.path.split(".")[0];
  const sectionRows = sections.get(section) ?? [];
  sectionRows.push(row);
  sections.set(section, sectionRows);
}

const lines = [
  "# OpenClaw Configuration Reference",
  "",
  `Generated from OpenClaw commit \`${version}\`.`,
  "",
  "This index reflects the canonical Zod schema in `vendor/openclaw/src/config/zod-schema*.ts` and",
  "`vendor/openclaw/src/config/types.openclaw.ts`. OpenClaw remains authoritative for defaults,",
  "cross-field validation, transforms, and provider-specific runtime behavior.",
  "",
  "> Generated by `pnpm run generate:openclaw-config-doc`. Do not edit by hand.",
  "",
];

for (const [section, sectionRows] of sections) {
  lines.push(
    `## \`${section}\``,
    "",
    "| Path | Type | Required | Constraints |",
    "| --- | --- | --- | --- |",
  );
  for (const row of sectionRows) {
    lines.push(
      `| \`${row.path}\` | ${row.type.replaceAll("|", "\\|")} | ${row.required ? "yes" : "no"} | ${row.notes} |`,
    );
  }
  lines.push("");
}

await writeFile(path.join(repoRoot, "docs", "OPENCLAW_CONFIG.md"), `${lines.join("\n")}\n`, "utf8");
console.log(`Generated docs/OPENCLAW_CONFIG.md (${uniqueRows.length} schema paths)`);
